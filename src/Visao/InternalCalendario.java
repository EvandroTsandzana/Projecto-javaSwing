    private void btgravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btgravarActionPerformed
      if(txtjornada.getText().isEmpty()){
          JOptionPane.showMessageDialog(null,"Campo Jornada Vazio");
          txtjornada.requestFocus();
      }else
          if(txtjogo.getText().isEmpty()){
              JOptionPane.showMessageDialog(null,"campo jogo Vazio");
              txtjogo.requestFocus();
          }else
              if(txtdata.getText().isEmpty()){
                  JOptionPane.showMessageDialog(null,"Campo Data vazio");
                  txtdata.requestFocus();
              }else
                  if(txtcampo.getText().isEmpty()){
                      JOptionPane.showMessageDialog(null,"Campo vazio");
                      txtcampo.requestFocus();
                  }else
                      if(flag==1){
                          cal.setJornada(txtjornada.getText());
                          cal.setJogo(txtjogo.getText());
                          cal.setDatahorario(txtdata.getText());
                          cal.setCampo(txtcampo.getText());
                          cal.setModalidade(verificarMod((String)cbmod.getSelectedItem()));
                          cal.setEscalao(VerificarEsc((String)cbesc.getSelectedItem()));
                          cont.gravar(cal);
                           btnovo.setEnabled(true);
                          txtcampo.setText("");
                          txtdata.setText("");
                          txtjogo.setText("");
                           txtjornada.setText("");
                          btcancelar.setEnabled(false);
                          btgravar.setEnabled(false);
                      }else{
                          cal.setJornada(txtjornada.getText());
                          cal.setJogo(txtjogo.getText());
                          cal.setDatahorario(txtdata.getText());
                          cal.setCampo(txtcampo.getText());
                          cal.setModalidade(verificarMod((String)cbmod.getSelectedItem()));
                          cal.setEscalao(VerificarEsc((String)cbesc.getSelectedItem()));
                          cont.editar(cal);
                          btnovo.setEnabled(true);
                          txtcampo.setText("");
                          txtdata.setText("");
                          txtjogo.setText("");
                           txtjornada.setText("");
                          btcancelar.setEnabled(false);
                          btgravar.setEnabled(false);
                      }
    }//GEN-LAST:event_btgravarActionPerformed

