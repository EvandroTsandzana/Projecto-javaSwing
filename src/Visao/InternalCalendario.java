   public void carregaModalidade(){
       try{
           ResultSet rs=cont.Modalidade();
           rs.beforeFirst();
           while(rs.next()){
               Modalidade mod=new Modalidade();
               mod.setCodigo(rs.getInt("CodigoM"));
               mod.setNome(rs.getString("nome"));
               try{
                   cbmod.addItem((String)(Object)(mod.getNome()));
               }catch(NullPointerException e){
                   
               }
           }
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null,"Erro ao carregar dados");
       }
   }
