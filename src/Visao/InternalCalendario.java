    public int verificarMod(String dado){
        conex.Conexao();
        ResultSet rs=cont.Modalidade();
        int id=0;
        try{
            rs.beforeFirst();
            while(rs.next()){
                if(rs.getString("nome").equalsIgnoreCase(dado)){
                    id=rs.getInt("codigoM");
                    break;
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Erro na verificacao do dado\n"+ex.getMessage());
        }
        return id;
    }
    
    public int VerificarEsc(String dado){
        conex.Conexao();
        ResultSet rs=cont.escalao();
        int id=0;
        try{
            rs.beforeFirst();
            while(rs.next()){
                if(rs.getString("nome").equalsIgnoreCase(dado)){
                    id=rs.getInt("CodigoE");
                    break;
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Erro na verificacao do dado\n"+ex.getMessage());
        }
        return id;
    }
        
    
    
